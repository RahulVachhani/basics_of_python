# Number System

# binary 
a = bin(25)    # Convert 25 in binary 
print(a)
print(type(a))


b = 0b1100       # store 11 in form of binary
print(b)
print(type(b))


# Octal
a = oct(50)    # Covert 50 into Octal
print(a)

b = 0o62       # Store 50 in form of Octal
print(b)


# Hexa Decimal
a = hex(100)      # Covert 100 into Hexa Decimal
print(a)

b = 0x64        # Store 100 in form of Hexa Decimal
print(b)



