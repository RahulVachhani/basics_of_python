# Conditional Statements 

# If Statement

a = 10

# If condition is true then only print
if a > 5:       
    print("The value of a is greater then 5")



# If With Else 

# If condition is true, the code inside the if block is executed
# Otherwise, the code inside the else block is executed
b = 10
if b > 100:         
    print("The value of b is greater then 100") 
else:
    print("The value of b is less then 100")



# If-Elif-Else ladder

c = 1000
if c == 100:
    print("The value of c is 100")

elif c == 1000:
    print("The value of c is 1000")

else:
    print("The value of c is greater then 100")