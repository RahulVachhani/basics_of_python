# Python Data types


# Int
a = 10  
print(type(a))



# Float
b = 10.22
print(type(b))



# Boolean  --> Its only True or False
c = True
print(type(c))

c = False
print(type(c))



# String  --> Collection Of Character
d = "Rahul"
print(type(d))


# Comlex 
e = 10 + 2j
print(type(d))



# List

l1 = [10, "Rahul", True]
print(type(l1))


# Tuple
t1 = (10, 11.22, "Rahul")
print(type(t1))


# Dictionary
d1 = {1:"Rahul", 2:"Rohit", 3:"Mohit"}
print(type(d1))


# Set
s1 = {1,2,3,4,5}
print(type(s1))




# type Conversion

a = True
b = int(a)
print(b)

b = float(a)
print(b)

b = bool(1)
print(b)